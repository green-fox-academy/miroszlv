public class TwoNumbers {

    public static void main(String[] args) {

        int a = 13;
        a += 22;
        System.out.println(a);

        int b = 22;
        b -= 13;
        System.out.println(b);

        int c = 22;
        c *= 13;
        System.out.println(c);

        int d = 22;
        d /= 13;
        System.out.println(d);


    }
}
